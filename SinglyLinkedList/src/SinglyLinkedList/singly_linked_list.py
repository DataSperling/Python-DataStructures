class SinglyLinkedList:
    """A singly linked list"""

    def __init__(self):
        """Constructor for empty singlyLinkedList"""
        self.head = None

    @property
    def data(self):
        """Getter for data in Node"""
        return self.data

    @data.setter
    def data(self, new_data):
        """Setter to add new_data to Node"""
        self.data = new_data


